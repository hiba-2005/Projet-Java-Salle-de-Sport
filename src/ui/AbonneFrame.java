    private void deletbnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletbnActionPerformed
        // TODO add your handling code here:
       int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this,
                "Sélectionne un abonné dans le tableau d'abord.",
                "Info",
                JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    // récupérer le modèle et l'id
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int idAbonne = Integer.parseInt(model.getValueAt(row, 0).toString());

    int reponse = JOptionPane.showConfirmDialog(
            this,
            "Voulez-vous vraiment supprimer cet abonné ?",
            "Confirmation",
            JOptionPane.YES_NO_OPTION
    );

    if (reponse == JOptionPane.YES_OPTION) {
        // chercher l'abonné en base
        Abonne abonne = fs.findById(idAbonne);

        if (abonne != null) {
            fs.delete(abonne);   // ICI on passe un Abonne, pas un int
            load();
            vider();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Abonné introuvable en base.",
                    "Erreur",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    }//GEN-LAST:event_deletbnActionPerformed

