    private void updatebnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebnActionPerformed
        // TODO add your handling code here:
          // 1) Récupérer la ligne sélectionnée
    int row = jTable1.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this,
                "Sélectionne un abonné dans le tableau d'abord.",
                "Info",
                JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    // 2) Récupérer les champs
    String nom    = nomtxt.getText().trim();
    String prenom = prenomtxt.getText().trim();
    String ageStr = agetxt.getText().trim();
    String sexe   = sexetxt.getSelectedItem().toString();

    // 3) Vérifier que rien n'est vide
    if (nom.isEmpty() || prenom.isEmpty() || ageStr.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Nom, prénom et âge sont obligatoires.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 4) Convertir l'âge en int avec contrôle
    int age;
    try {
        age = Integer.parseInt(ageStr);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this,
                "L'âge doit être un nombre entier.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 5) Récupérer l'id dans la première colonne du tableau
    int idAbonne = (int) model.getValueAt(row, 0);

    // 6) Confirmation
    int reponse = JOptionPane.showConfirmDialog(this,
            "Voulez-vous vraiment modifier cet abonné ?",
            "Confirmation",
            JOptionPane.YES_NO_OPTION);

    if (reponse == JOptionPane.YES_OPTION) {
        // On crée un objet Abonne complet (avec id)
        Abonne a = new Abonne(idAbonne, nom, prenom, age, sexe);

        // Appel du service
        fs.update(a);

        // Recharger le tableau et vider les champs
        load();
        vider();
    }
        
    }//GEN-LAST:event_updatebnActionPerformed

