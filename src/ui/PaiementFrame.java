    private void RecherchebnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RecherchebnActionPerformed
        // TODO add your handling code here:
        String nomStr = nomtxt.getText().trim().toLowerCase();
        String prenomStr = prenomtxt.getText().trim().toLowerCase();

        modelAbonne.setRowCount(0);

        for (Abonne a : allAbonnes) {
            boolean okNom = nomStr.isEmpty() || a.getNom().toLowerCase().contains(nomStr);
            boolean okPrenom = prenomStr.isEmpty() || a.getPrenom().toLowerCase().contains(prenomStr);

            if (okNom && okPrenom) {
                modelAbonne.addRow(new Object[]{
                    a.getId(), a.getNom(), a.getPrenom(), a.getAge(), a.getSexe()
                });
            }
        }
    }//GEN-LAST:event_RecherchebnActionPerformed

