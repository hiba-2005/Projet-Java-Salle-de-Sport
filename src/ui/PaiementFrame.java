    // ================= CHARGEMENT DES DONNÉES =================

    private void loadAllAbonnes() {
        allAbonnes = abonneService.findAll();
        modelAbonne.setRowCount(0);

        for (Abonne a : allAbonnes) {
            modelAbonne.addRow(new Object[]{
                a.getId(), a.getNom(), a.getPrenom(), a.getAge(), a.getSexe()
            });
        }
    }

    private void loadPaiements() {
        modelPaiement.setRowCount(0);

        for (Paiement p : paiementService.findAll()) {
            Abonne a = p.getAbonne();
            String nomComplet = (a != null) ? a.getNom() + " " + a.getPrenom() : "";
            LocalDate d = p.getDatePaiement();

            modelPaiement.addRow(new Object[]{
                p.getId(),
                nomComplet,          // colonne "idabonne" mais on affiche nom + prénom
                d,
                p.getMontant()
            });
        }
    }

    // ================= SELECTION DANS LES TABLES =================

    private void selectAbonneFromTable() {
        int row = jTable1.getSelectedRow();
        if (row == -1) return;

        int idAbonne = (int) jTable1.getValueAt(row, 0);
        currentAbonne = abonneService.findById(idAbonne);
    }

    private void selectPaiementFromTable() {
        int row = jTable2.getSelectedRow();
        if (row == -1) return;

        int id = (int) jTable2.getValueAt(row, 0);
        Paiement p = paiementService.findById(id);
        if (p == null) return;

        currentPaiement = p;
        currentAbonne = p.getAbonne();

        // remplir les champs
        montanttxt.setText(String.valueOf(p.getMontant()));
        LocalDate d = p.getDatePaiement();
        if (d != null) {
            DateChooser2.setDate(java.sql.Date.valueOf(d));
        }
    }
